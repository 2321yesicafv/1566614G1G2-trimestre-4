package co.edu.sena.programming.capitulo02;

public class Ejemplo02 {
    public static void main(String[] args) {
        //literales numericos
        float a = 23.5F;

        long b = 32_423_467_876L;

        // literales para binarios
        int binario = 0b1010101;
        System.out.println(binario);

        // hexadecimales
        int heaxadecimal = 0xFFFF;
        System.out.println(heaxadecimal);

        // octal
        int octal = 071;
        System.out.println(octal);
    }
}
