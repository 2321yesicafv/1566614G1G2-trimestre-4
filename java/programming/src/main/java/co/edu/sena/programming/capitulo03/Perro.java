package co.edu.sena.programming.capitulo03;

public class Perro {
    private int id;
    private String nombre;

    public Perro(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
}
